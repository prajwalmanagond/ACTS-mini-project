const mongoose = require('mongoose');

const TaskSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  createdDate: {
    type: Date,
    default: Date.now
  },
  endDate: {
    type: Date,
    required: true
  },
  status: {
    type: String,
    enum: ['Upcoming', 'Completed', 'Overdue'],
    default: 'Upcoming'
  }
});

module.exports = mongoose.model('Task', TaskSchema);
